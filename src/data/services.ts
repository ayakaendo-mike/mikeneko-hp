/**
 * サービス定義（Single Source of Truth）
 * /service/ 一覧・/service/[slug]/ 個別ページ・トップのサービスカードの元データ。
 * SEO: 個別ページはピラーページ（docs/03-seo-content-strategy.md 1章）。
 *   title は「地域＋サービス」の複合キーワード（docs 3-3 雛形）。
 * 注: DX支援は当面サービス提供外のため定義しない（2026-07-06 実機レビューで決定）。
 */

export interface Service {
  /** URL: /service/[slug]/ */
  slug: string;
  /** 表示順・カード番号（S-01等） */
  no: string;
  /** カード用アイコン文字 */
  icon: string;
  /** サービス名 */
  title: string;
  /** 英字ラベル */
  en: string;
  /** titleタグ用（地域込み。「｜みけねこデザイン」はテンプレートで付与） */
  seoTitle: string;
  /** meta description */
  seoDescription: string;
  /** 一覧カード・トップカード用の短い説明 */
  summary: string;
  /** 個別ページのリード文 */
  lead: string;
  /** こんな方に（お悩み） */
  targets: string[];
  /** 提供内容 */
  offers: { name: string; desc: string }[];
  /** みけねこデザインらしさ（伴走ポイント） */
  point: string;
  /** トップページのサービスカードに出すか */
  featured: boolean;
}

